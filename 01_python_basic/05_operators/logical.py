# Logical operators are used to combine conditional statements
a = True
b = False

print(a and b)
print(a or b)
print(not a)

# Bitwise operators act on bits and perform bit-by-bit
a = 10
b = 4
print(a & b)
print(a | b)
print(~a)
print(a ^ b)
print(a >> 2)
print(a << 2)

# Assignment operators are used to assign values to the variables
a = 10
b = a
print(b)
b += a
print(b)
b -= a
print(b)
b *= a
print(b)
b <<= a
print(b)